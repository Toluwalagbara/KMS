public class monthlyreportmodel {

    private String Activity;
    private String MajorAchievments;
    private String Challenges;
    private String MajorAdaptation;
    private String LessonsLearned;
    private String Recommendation;
    private String PlansForComingWeek;

    public String getActivity() {
        return Activity;
    }

    public void setActivity(String Activity) {
        this.Activity = Activity;
    }

    public String getMajorAchievments() {
        return MajorAchievments;
    }

    public void setMajorAchievments(String MajorAchievments) {
        this.MajorAchievments = MajorAchievments;
    }

    public String getChallenges() {
        return Challenges;
    }

    public void setChallenges(String Challenges) {
        this.Challenges = Challenges;
    }

    public String getMajorAdaptation() {
        return MajorAdaptation;
    }

    public void setMajorAdaptation(String MajorAdaptation) {
        this.MajorAdaptation = MajorAdaptation;
    }

    public String getLessonsLearned() {
        return LessonsLearned;
    }

    public void setLessonsLearned(String LessonsLearned) {
        this.LessonsLearned = LessonsLearned;
    }

    public String getRecommendation() {
        return Recommendation;
    }

    public void setRecommendation(String Recommendation) {
        this.Recommendation = Recommendation;
    }

    public String getPlansForComingWeek() {
        return PlansForComingWeek;
    }

    public void setPlansForComingWeek(String PlansForComingWeek) {
        this.PlansForComingWeek = PlansForComingWeek;
    }
    
    
    
}
